    public class MockingRobot : IRobot
    {
        private readonly IEnumerable<(Command, long)> _data;
        
        public MockingRobot(IEnumerable<long> data)
        {
            _data = data.Select(MapCommand);

            (Command, long) MapCommand(long x, int i)
            {
                return (i % 2 == 0 ? Command.Paint : Command.Move, x);
            }
        }

        public IEnumerable<(Command, long)> Run() => _data;
    }


